### Um funcionário foi sorteado para receber um aumento de 15% em seu salário.
## Calcule o valor do novo salário deste funcionário.

salario = float(input('Informe seu salario : '))
aumento = salario + (salario * 0.15) ## mostra o salario com aumento de 15%

print(f'Seu Salario atual é de {salario}  com aumento passou a ser de {aumento}') ## imprime o salario atual e logo em seguida o aumento