## Exercicio Lista 1
# Crie um programa que receba os valores de três notas de
# um aluno e imprima a média.

nota1 = float(input('Informe sua nota:')) ## 3 notas de um usuario e dsejo imprimiir a media do aluno
nota2 = float(input('Informe outra nota:'))
nota3 = float(input('Informe novamente:'))


media = (nota1 + nota2+ nota3) / 3 ## dividindo a nota do aluno por 3
print(f'A media do aluno é de {media}') ## imprimindo a media do aluno