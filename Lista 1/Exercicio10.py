## Peça dois números inteiros e mostre o resultado da divisão inteira e o resto.
numero = int(input('Informe um Numero:'))
metade = numero // 2 ## metodo para  retornar a divisao inteira
print(metade)
resto  = numero % 2 ## mostra o resto da divisao do numero por 2
print(resto)
